#Complex, Binary and Hexadecimal types

#Complex type
a = 2 + 5j

#Binary type
#Will print the decimal value of whatever binary number comes after '0B'
b = 0B1010

#Hexadecimal
#Will print the decimal value of whatever hexadecimal number comes after '0X'
c = 0XA

d = 0O12

#Boolean type
myBoolean = True

print(type(a))
print(b)
print(c)
print(d)
print(type(myBoolean))
