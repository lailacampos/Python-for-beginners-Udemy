# Using the inbuilt math module
# https://docs.python.org/3/library/math.html
import math

print(math.floor(7.6))  # Return the floor of x, the largest integer less than or equal to x.
print(math.ceil(8.4))  # Return the ceiling of x, the smallest integer greater than or equal to x.
print(math.sqrt(9))  # Return the square root of x.
print(math.fabs(-2.2))  # Return the absolute value of x.
# ...
